package escola;

public class Aluno {
    private String nome;
    private int idade;

    //metodo get é responsavelpor obter o valor atual do objeto
    public String getNome() {
        return nome;
    }

    //metodo set é responsavel por modificar o valor do objeto
    public void setNome(String nome) {
        this.nome = nome;
    }

    //metodo get é responsavelpor obter o valor atual do objeto
    public int getIdade() {
        return idade;
    }

    //metodo set é responsavel por modificar o valor do objeto
    public void setIdade(int idade) {
        this.idade = idade;
    }
}
